    private void jButtonVerBDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVerBDActionPerformed
        DefaultTableModel model = (DefaultTableModel) jTableBD.getModel();
        model.setRowCount(0);
        try{ 
        Class.forName("org.gjt.mm.mysql.Driver");
        //Class.forName("com.mysql.jdbc.Driver");
        
        con=DriverManager.getConnection("jdbc:mysql://localhost/tarea1","root","");
        stmt=con.createStatement(); 
        SQL="select * from alumno";
        resultado=stmt.executeQuery(SQL);
        
        while(resultado.next()){
            String rut=resultado.getString("rut");
            String nombre= resultado.getString("nombre");
            String apellido = resultado.getString("apellido");
            int fecha_nacimiento = resultado.getInt("fecha_nacimiento");
            String direccion = resultado.getString("direccion");
            String telefono = resultado.getString("telefono");
            String carrera = resultado.getString("carrera");
            int anio_ingreso = resultado.getInt("anio_ingreso");
            
            String strFechaNacimiento = String.valueOf(fecha_nacimiento);
            String strAnioIngreso = String.valueOf(anio_ingreso);
            
            String[] datos = {rut, nombre, apellido, strFechaNacimiento, direccion, telefono, carrera, strAnioIngreso};
            model.addRow(datos);
        }
        con.close();
        }catch(ClassNotFoundException e){
            System.out.println("No encuentra clase " + e.getMessage());
        }catch(SQLException e){
            System.out.println("Error conexion " + e.getMessage());
        }
        
    }//GEN-LAST:event_jButtonVerBDActionPerformed

